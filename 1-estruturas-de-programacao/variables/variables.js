// declarando variaveis
// var -> metodo descontinuado para declaracao de variaveis em js
// const -> valor que nunca muda
// let -> declaracao atual de variaveis em js
let orange = 2;
const red = 3;
var blue = 4;
// shorthand na declaracao de variaveis
let name1 = "Felipe",
  name2 = "Mateus",
  idade = 22;

// utilizando variaveis com statments
console.log(`Orange * Red = ${orange * red}`);
console.log(`O valor de blue e: ${blue}`);
console.log(`Meu nome e ${name1} ${name2}, e tenho ${idade} anos!`);

